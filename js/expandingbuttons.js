document.getElementById("resprodbutton").onclick = function() {
	if ( $('#resourceprodarrow').hasClass('glyphicon glyphicon-collapse-up') ) {
		$('#resourceprodarrow').removeClass('glyphicon glyphicon-collapse-up');
		$('#resourceprodarrow').addClass('glyphicon glyphicon-collapse-down');
		$('#resprodbutton').removeClass('btn btn-arrow-open');
		$('#resprodbutton').addClass('btn btn-arrow-closed');
		return;
	}else if ( $('#resourceprodarrow').hasClass('glyphicon glyphicon-collapse-down') ) {
		$('#resourceprodarrow').removeClass('glyphicon glyphicon-collapse-down');
		$('#resourceprodarrow').addClass('glyphicon glyphicon-collapse-up');
		$('#resprodbutton').removeClass('btn btn-arrow-closed');
		$('#resprodbutton').addClass('btn btn-arrow-open');
		return;
	}
}

